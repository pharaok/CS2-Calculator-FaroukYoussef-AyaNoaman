#include "calculator.h"


template<typename T>
T add(const T& a, const T& b) {
    return a+b;
}


template<typename T>
T sub(const T& a, const T& b) {
    return a-b;
}


template<typename T>
T mult(const T& a, const T& b) {
    return a/b;
}


template<typename T>
T div(const T& a, const T& b) {
    return a/b;
}